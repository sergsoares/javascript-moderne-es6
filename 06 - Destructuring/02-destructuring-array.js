/*
  Destructuring - Permite que possamos extrair elementos, variaveis, objetos de
  forma mais facil..

  let, const [ elements ] = for
*/

const array = ['William', 'Goulart', 23, 'Campo Grande']
const [ name, lastName, age, city ] = array

console.log(name) // William
console.log(lastName) // Goulart
console.log(age) // 26
console.log(city) // Campo Grande
